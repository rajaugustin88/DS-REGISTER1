// --- Countdown Timer Component ---
function CountdownTimer({ trialEndDate, onExpire }: { trialEndDate: string, onExpire: () => void }) {
  const [timeLeft, setTimeLeft] = useState<{ days: number, hours: number, minutes: number, seconds: number } | null>(null);

  useEffect(() => {
    const calculateTimeLeft = () => {
      const now = new Date().getTime();
      const end = new Date(trialEndDate).getTime();
      const diff = end - now;

      if (diff <= 0) {
        setTimeLeft({ days: 0, hours: 0, minutes: 0, seconds: 0 });
        onExpire();
        return;
      }

      const days = Math.floor(diff / (1000 * 60 * 60 * 24));
      const hours = Math.floor((diff % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
      const minutes = Math.floor((diff % (1000 * 60 * 60)) / (1000 * 60));
      const seconds = Math.floor((diff % (1000 * 60)) / 1000);

      setTimeLeft({ days, hours, minutes, seconds });
    };

    calculateTimeLeft();
    const timer = setInterval(calculateTimeLeft, 1000);
    return () => clearInterval(timer);
  }, [trialEndDate, onExpire]);

  if (!timeLeft) return null;

  return (
    <div className="flex items-center gap-2 bg-primary/10 px-4 py-2 rounded-xl border border-primary/20 shadow-sm animate-pulse">
      <Clock className="w-4 h-4 text-primary" />
      <span className="text-[0.65rem] font-black text-primary uppercase tracking-widest">
        Trial Ends In: {timeLeft.days}d {timeLeft.hours.toString().padStart(2, '0')}h {timeLeft.minutes.toString().padStart(2, '0')}m {timeLeft.seconds.toString().padStart(2, '0')}s
      </span>
    </div>
  );
}

