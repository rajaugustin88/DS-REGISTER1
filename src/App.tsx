function ProtectedRoute({ children }: { children: React.ReactNode }) {
  const [user, setUser] = useState<FirebaseUser | null>(null);
  const [loading, setLoading] = useState(true);
  const [termsAccepted, setTermsAccepted] = useState<boolean | null>(null);
  const navigate = useNavigate();

  useEffect(() => {
    const unsubscribe = onAuthStateChanged(auth, async (u) => {
      setUser(u);
      if (u) {
        // Terms & Conditions one-time acceptance logic.
        const accepted = await checkTermsAccepted(u.uid);
        setTermsAccepted(accepted);
      } else {
        navigate('/');
      }
      setLoading(false);
    });
    return () => unsubscribe();
  }, [navigate]);

  const handleAcceptTerms = async () => {
    if (user) {
      await acceptTerms(user.uid);
      setTermsAccepted(true);
    }
  };

  const handleLogout = async () => {
    await signOut(auth);
    navigate('/');
  };

  if (loading || (user && termsAccepted === null)) {
    return (
      <div className="flex items-center justify-center min-h-screen">
        <div className="w-12 h-12 border-4 border-primary border-t-transparent rounded-full animate-spin"></div>
      </div>
    );
  }

  if (user && termsAccepted === false) {
    return <TermsModal isOpen={true} onAccept={handleAcceptTerms} onLogout={handleLogout} />;
  }

  return user ? <>{children}</> : null;
}

